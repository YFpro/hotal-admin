    /**
     * 保存新建的资源
     *
     *添加未支付订单
     */
    public function save(Request $request)
    {
        $data = $this->request->post();
        $data['status']=1;
        $stayhomeModel = model('stayhome');
        $stayhome = $stayhomeModel->queryOne(['sid'=>$data['sid']],'status');
        if(!$stayhome['status']){
            return json([
                'code'=>200,
                'msg'=>'该名宿已被预定',
                'data'=>$stayhome
            ]);
        }
        Db::startTrans();
        try {
            $orderResult= $this->model->add($data);
            $lastid =$this->model->getLastInsID();
            $stayhomeResult= $stayhomeModel->edit(['status'=>0],['sid'=>$data['sid']]);
            if($stayhomeResult && $orderResult) {
                Db::commit();
                return json([
                    'code' => 200,
                    'msg' => '下单成功',
                    'oid' => $lastid,
                ]);
            }else{
                return json([
                    'code'=>200,
                    'msg'=>'下单失败',
                ]);
            }
        }catch (Exception $exception){
            Db::rollback();
            return json([
                'code'=>200,
                'msg'=>'下单失败',
            ]);

    }
    }

