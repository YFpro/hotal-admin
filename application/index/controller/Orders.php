    public function index()
    {
        $data =$this->request->get();
        if(!$data['typeorder']){
            $result= Db::table('orders')->where('uid',$data['uid'])->select();
            if($result){
                return json([
                    'code'=>200,
                    'msg'=>'数据获取成功',
                    'data'=>$result,
                ]);
            }else{
                return json([
                    'code'=>404,
                    'msg'=>'暂无数据',
                ]);
            }
        }else {
            $where = [];
            $where['uid']=$data['uid'];
            $where['status'] = $data['typeorder'];
            $result = Db::table('orders')->where($where)->select();
            if ($result) {
                return json([
                    'code' => 200,
                    'msg' => '数据获取成功',
                    'data' => $result,
                    'where' => $where
                ]);
            } else {
                return json([
                    'code' => 200,
                    'msg' => '暂无数据',
                ]);
            }
        }
    }

